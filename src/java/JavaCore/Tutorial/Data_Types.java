/*
   Data Types in Java:
       1. Kiểu dữ liệu nguyên thủy.
       2. kiểu dữ liệu không nguyên thủy.


   1. kiểu dữ liệu nguyên thủy:
       + boolean data type:    boolean b = false;
        + byte data type:       byte a = 10;
        + char data type:       char A = 'B';
        + short data type:      short s = 10000; short r = -5000;
        + int data type:        int ar = 100000;
        + long data type:       long a = 1000000L; long b = -200000L;
        + float data type:      float f = 234.5f;
        + double data type:     double d = 657.123;


   2. Kiểu dữ liệu không nguyên thủy:
       + classes
       + interfaces
       + arrays.

*/

package src.java.JavaCore.Tutorial;

public class Data_Types {
    public static void main(String[] args) {

        char a = 'A';
        boolean b = false;
        byte c = 10;
        double d = 657.123;
        float f = 234.5f;
        int ar = 100000;
        short s = 10000;

        System.out.println("Char: " + a);
        System.out.println("Boolean: " + b);
        System.out.println("Byte: " + c);
        System.out.println("Double: " + d);
        System.out.println("Float: " + f);
        System.out.println("Short: " + s);

    }
}
